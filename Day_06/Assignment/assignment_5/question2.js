function add(a,b){
    let sum = a+b;
    console.log(`Addition : `+sum);
}
add(5,10);

function sub(a,b){
    let diff = a-b;
    console.log(`Subtraction : `+diff);
}
sub(50,10);

function mul(a,b){
    let mul = a*b;
    console.log(`Multiplication : `+mul);
}
mul(5,10);

function div(a,b){
    let div = a/b;
    console.log(`Division : `+div);
}
div(50,10);

function mod(a,b){
    let mod = a%b;
    console.log(`Modulus : `+mod);
}
mod(50,5);